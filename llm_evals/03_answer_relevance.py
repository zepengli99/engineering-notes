"""
Answer Relevance — does the answer actually address the question?

Reference-free: no ground-truth answer needed.

Algorithm (RAGAS):
  1. Generate n questions from the answer (pretend the original question is unknown)
  2. Embed the original question and all generated questions
  3. Answer Relevance = mean cosine similarity between original and generated

Key insight: if an answer talks about the right things, you can reverse-engineer
the original question from it. If the answer wanders off-topic, the generated
questions will diverge from the original — cosine similarity drops.

Scenario A: on-topic answer → high relevance
Scenario B: off-topic answer (answers a different question) → low relevance
"""

import numpy as np
from common import DOCS, load_embedder, build_index, retrieve, generate, make_llm, parse_json

QUESTION = "How long does NovaTech store my API request logs?"

# Off-topic: answers a pricing question instead
OFF_TOPIC_ANSWER = (
    "NovaTech offers three pricing tiers. The Basic plan costs $29/month, "
    "the Pro plan costs $99/month, and Enterprise is custom-priced."
)

# ── prompt ───────────────────────────────────────────────────────────────

GENERATE_QUESTIONS_PROMPT = """\
Given the answer below, generate {n} distinct questions that this answer could be responding to.
Output a JSON array of strings. No explanation, no markdown.

Answer: {answer}"""

# ── metric ───────────────────────────────────────────────────────────────

def generate_questions(answer: str, n: int, llm) -> list[str]:
    response = llm.invoke(GENERATE_QUESTIONS_PROMPT.format(answer=answer, n=n))
    return parse_json(response.content)

def answer_relevance(question: str, answer: str, n: int, llm, embedder) -> dict:
    generated_qs = generate_questions(answer, n=n, llm=llm)

    # embed original + generated together in one call (efficient)
    all_questions = [question] + generated_qs
    embeddings = embedder.encode(all_questions, normalize_embeddings=True)

    original_emb = embeddings[0]
    generated_embs = embeddings[1:]

    # cosine similarity = dot product for unit vectors
    similarities = [float(np.dot(original_emb, g)) for g in generated_embs]

    return {
        "score": float(np.mean(similarities)),
        "generated_questions": generated_qs,
        "similarities": similarities,
    }

def print_result(result: dict):
    print("Generated back-questions:")
    for q, s in zip(result["generated_questions"], result["similarities"]):
        print(f"  [{s:.3f}] {q}")
    print(f"\n  Answer Relevance: {result['score']:.3f}")

# ── run ──────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    embedder = load_embedder()
    index = build_index(DOCS, embedder)
    llm = make_llm()

    chunks = retrieve(QUESTION, k=2, index=index, docs=DOCS, embedder=embedder)
    on_topic_answer = generate(QUESTION, chunks, llm)

    print(f"Original question: {QUESTION}\n")

    # ── scenario A: on-topic answer ───────────────────────────────────
    print("=" * 60)
    print("SCENARIO A: on-topic answer (generated by RAG pipeline)")
    print("=" * 60)
    print(f"\nAnswer: {on_topic_answer}\n")
    print_result(answer_relevance(QUESTION, on_topic_answer, n=3, llm=llm, embedder=embedder))

    # ── scenario B: off-topic answer ──────────────────────────────────
    print("\n" + "=" * 60)
    print("SCENARIO B: off-topic answer (talks about pricing, not log retention)")
    print("=" * 60)
    print(f"\nAnswer: {OFF_TOPIC_ANSWER}\n")
    print_result(answer_relevance(QUESTION, OFF_TOPIC_ANSWER, n=3, llm=llm, embedder=embedder))
    print("\n  (back-questions ask about pricing → low similarity to original)")
