"""
Faithfulness — does the answer hallucinate, or is every claim grounded in context?

Reference-free: only needs the retrieved context and the answer.
No ground-truth answer required, so this can run on live traffic.

Algorithm:
  1. Extract every factual claim from the answer (LLM call)
  2. For each claim, ask: can this be inferred from the context alone? (LLM call per claim)
  3. Faithfulness = supported_claims / total_claims

Scenario A: answer generated by the RAG pipeline — should be fully faithful.
Scenario B: answer with one fabricated claim — score should drop.
"""

from common import DOCS, load_embedder, build_index, retrieve, generate, make_llm, parse_json

QUESTION = "What does the Pro plan include and how much does it cost?"

# The fabricated claim: "free 7-day trial" — not in any document
HALLUCINATED_ANSWER = (
    "The NovaTech Pro plan costs $99/month. It includes 10,000 API calls per day, "
    "priority support with a 4-hour response time, and access to Nova-1 and Nova-2 models. "
    "New subscribers also receive a free 7-day trial before the first charge."
)

# ── prompts ─────────────────────────────────────────────────────────────

EXTRACT_CLAIMS_PROMPT = """\
List every distinct factual claim in the answer below.
Output a JSON array of strings, one claim per item. No explanation, no markdown.

Answer: {answer}"""

VERIFY_CLAIM_PROMPT = """\
Context:
{context}

Claim: {claim}

Can this claim be directly inferred from the context above?
Output only JSON: {{"supported": true}} or {{"supported": false}}"""

# ── metric ──────────────────────────────────────────────────────────────

def extract_claims(answer: str, llm) -> list[str]:
    response = llm.invoke(EXTRACT_CLAIMS_PROMPT.format(answer=answer))
    return parse_json(response.content)

def verify_claim(claim: str, context: str, llm) -> bool:
    response = llm.invoke(VERIFY_CLAIM_PROMPT.format(context=context, claim=claim))
    return parse_json(response.content).get("supported", False)

def faithfulness(answer: str, chunks: list[dict], llm) -> dict:
    context = "\n\n".join(f"[{c['id']}] {c['text']}" for c in chunks)
    claims = extract_claims(answer, llm)

    results = []
    for claim in claims:
        supported = verify_claim(claim, context, llm)
        results.append({"claim": claim, "supported": supported})

    score = sum(r["supported"] for r in results) / len(results) if results else 0.0
    return {"score": score, "claims": results}

def print_result(result: dict):
    for item in result["claims"]:
        mark = "✓" if item["supported"] else "✗"
        print(f"  {mark} {item['claim']}")
    print(f"\n  Faithfulness: {result['score']:.2f}")

# ── run ─────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    embedder = load_embedder()
    index = build_index(DOCS, embedder)
    llm = make_llm()

    chunks = retrieve(QUESTION, k=2, index=index, docs=DOCS, embedder=embedder)
    context_ids = [c["id"] for c in chunks]

    print(f"Question:  {QUESTION}")
    print(f"Retrieved: {context_ids}\n")

    # ── scenario A: faithful answer ───────────────────────────────────
    print("=" * 60)
    print("SCENARIO A: answer generated by RAG pipeline")
    print("=" * 60)

    rag_answer = generate(QUESTION, chunks, llm)
    print(f"\nAnswer: {rag_answer}\n")
    print_result(faithfulness(rag_answer, chunks, llm))

    # ── scenario B: answer with hallucinated claim ────────────────────
    print("\n" + "=" * 60)
    print("SCENARIO B: answer with fabricated claim")
    print("=" * 60)

    print(f"\nAnswer: {HALLUCINATED_ANSWER}\n")
    print_result(faithfulness(HALLUCINATED_ANSWER, chunks, llm))
    print('\n  (the "free 7-day trial" claim is not in any retrieved chunk)')
